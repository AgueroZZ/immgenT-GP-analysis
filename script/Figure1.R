# Figure 1. Overview.
#
# Panels produced (final files figures/final-selected/bits/Figure 1/1A.pdf .. 1I.pdf;
# no separate caption file was found for Figure 1 during this refactor):
#   1A  Global MDE colored by major lineage (subsampled per lineage).
#   1B  Hand-finished schematic (Adobe Illustrator) -- NOT code-generated,
#       no source to port. Not produced by this script.
#   1C  Giant heatmap of all 200 GP loadings, cells stratified by lineage x organ.
#   1D  "Signature volcano" for GP1: each gene's normalized weight vs. mean
#       shifted-log expression, top genes labeled. NOT from the original
#       pre-refactor analysis code -- generated by the separate
#       "immgen-signature" Shiny app
#       (.../Immgen/webapps/immgen-signature/); see
#       code/R/volcano_helpers.R for the ported plotting function
#       and the caveat about n_label/threshold not being exactly recoverable.
#   1E  Histogram of the proportion of highly-active cells per GP.
#   1F  Histogram of the number of highly-active genes per GP.
#   1G  Boxplot of active-GP-count per cell, by major lineage.
#   1H  Boxplot of active-GP-count per cell, activated vs resting.
#   1I  Scatter of CD44 protein level vs. number of active GPs per cell.
# The original Figure_Overview.R produces 3 more outputs (raw
# active-cell-count histogram, active-gene proportion histogram, and a
# PVE-colored active-cell-vs-active-gene scatter) that don't match any
# remaining final-figure byte size -- saved under their descriptive names
# (not lettered) since they appear to not be part of the final 9-panel set.
#
# Source: ported from Figure_Overview.R (panels C, E-I) and
# Figure_Lineage.R's "MDE by Lineage" section (panel A, confirmed by
# an exact byte match between figures/final-selected/bits/Figure 1/1A.pdf
# and figures/Figure2_Lineage/UMAP_level1_group.pdf during this refactor).
#
# Required inputs (data/) -- see code/README.md's "Data provenance" table
# for the full picture:
#   flashier_snmf_summary.rds                [code/pipeline/01_extract_data.R]
#   L_pm_filtered.rds, F_pm_filtered.rds      [code/pipeline/01b_filter_cells.R]
#   igt1_96_..._ADTonly.Rds                   [primary input Seurat object]
#   protein_mat_normalized_lognorm.rds        [gap, no producer script here]
#   umap_result.rds                           [gap, no producer script here]
#   mean_shifted_log_expr.rds                 [gap, no producer script here]
#   flashier_snmf_fitted_prior.rda            [gap, no producer script here]

library(ggplot2)
library(dplyr)
library(scattermore)
