# ============================================================
# PVE-colored scatter of expected active-cell vs active-gene proportion
# (not one of the 9 lettered panels -- kept under its descriptive name)
# ============================================================
pve <- flashier_snmf_summary$pve
eps <- 0
pve_log <- log10(pve + eps)
pve_log_min <- quantile(pve_log, 0.02, na.rm = TRUE)
pve_log_max <- quantile(pve_log, 0.98, na.rm = TRUE)
pve_log_clipped <- pmin(pmax(pve_log, pve_log_min), pve_log_max)
pve_scaled <- (pve_log_clipped - min(pve_log_clipped)) / (max(pve_log_clipped) - min(pve_log_clipped))
n_col <- 100
pal <- colorRampPalette(c("lightgray", "skyblue", "darkblue"))(n_col)
idx_col <- pmin(pmax(1, floor(pve_scaled * (n_col - 1)) + 1), n_col)
col_points <- pal[idx_col]
load(paste0(data_path, "flashier_snmf_fitted_prior.rda"))
l_pi_vec <- sapply(1:200, function(i) flashier_snmf_fitted_prior$L_ghat[[i]]$pi[1])
f_pi_vec <- sapply(1:200, function(i) flashier_snmf_fitted_prior$F_ghat[[i]]$pi[1])
p_cells <- (1 - l_pi_vec)
p_genes <- (1 - f_pi_vec)

pdf(paste0(figure_path, "scatter_e_active_cells_vs_genes.pdf"), width = 6, height = 6)
plot(
  p_cells, p_genes,
  xlab = "Expected proportion of active cells", ylab = "Expected proportion of active genes",
  log = "xy", col = col_points, pch = 19, xaxt = "n", yaxt = "n",
  main = "Expected proportion of active cells vs genes per GP\n(colored by PVE)"
)
ticks <- c(0.0001, 0.001, 0.01, 0.05, 0.1, 0.3, 0.5, 1)
axis(1, at = ticks, labels = paste0(ticks * 100, "%"))
axis(2, at = ticks, labels = paste0(ticks * 100, "%"))
brks_pve <- c(1e-7, 1e-5, 1e-3, 1e-2, 0.1, 0.2)
brks_pve <- brks_pve[brks_pve >= min(pve) & brks_pve <= max(pve)]
brks_log <- log10(brks_pve + eps)
brks_log_clipped <- pmin(pmax(brks_log, pve_log_min), pve_log_max)
brks_scaled <- (brks_log_clipped - min(pve_log_clipped)) / (max(pve_log_clipped) - min(pve_log_clipped))
brks_idx <- pmin(pmax(1, floor(brks_scaled * (n_col - 1)) + 1), n_col)
brks_cols <- pal[brks_idx]
legend("bottomright", legend = format(brks_pve, scientific = TRUE, digits = 2), col = brks_cols, pch = 15, pt.cex = 1.5, title = "PVE", bty = "n")
dev.off()
